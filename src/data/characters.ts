// Helper function to get unique values for each attribute
export const getUniqueValues = (attribute: keyof Omit<Character, 'id' | 'image' | 'name'>) => {
  const values = characters.map(character => character[attribute]);
  return [...new Set(values)].filter(value => value !== null).sort();
};

